`fromname` VARCHAR(255) NULL,
categoryID INT DEFAULT 0,
rageID INT NULL,
seriesfull VARCHAR(15) NULL,
season VARCHAR(10) NULL,
episode VARCHAR(10) NULL,
`grabs` INT UNSIGNED NOT NULL DEFAULT '0',
